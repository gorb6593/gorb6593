package oop.inheritance;

public class Beverage {
	private String type;
	private int price;
	protected String getType(){
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
		
}
